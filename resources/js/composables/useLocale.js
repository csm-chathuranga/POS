function t(key) {
    return translations[locale.value]?.[key]
        ?? translations['si'][key]
        ?? key;
}

function toggleLocale() {
    const idx = LOCALES.indexOf(locale.value);
    locale.value = LOCALES[(idx + 1) % LOCALES.length];
    localStorage.setItem('locale', locale.value);
}

function setLocale(lang) {
    if (LOCALES.includes(lang)) {
        locale.value = lang;
        localStorage.setItem('locale', lang);
    }
}

function tBill(key) {
    return translations[billLocale.value]?.[key]
        ?? translations['si'][key]
        ?? key;
}

function setBillLocale(lang) {
    if (LOCALES.includes(lang)) {
        billLocale.value = lang;
        localStorage.setItem('billLocale', lang);
    }
}

export function useLocale() {
    return { locale: readonly(locale), t, toggleLocale, setLocale, billLocale: readonly(billLocale), tBill, setBillLocale };
}
